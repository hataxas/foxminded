// Random number in [0, max]
function randomInt(max) {
  return Math.round(Math.random() * max);
}

function getRandomRgb() {
  let r = randomInt(255);
  let g = randomInt(255);
  let b = randomInt(255);

  return `rgb(${r}, ${g}, ${b})`;
}

function getRandomHex() {
  let hexColor = `#${randomInt(0xffffff).toString(16)}`;

  return hexColor;
}

function getRandomColorName() {
  let colorName = cssColorNames[randomInt(cssColorNames.length - 1)];

  return colorName;
}

function changeBackgroundColor() {
  let randomNumber = randomInt(2);
  let backgroundColor = '';

  if(randomNumber === 0) {
    backgroundColor = getRandomRgb();
  } else if (randomNumber === 1) {
    backgroundColor = getRandomHex();
  } else {
    backgroundColor = getRandomColorName();
  }

  wrapper.style.backgroundColor = backgroundColor;
  colorName.innerHTML= backgroundColor;
}

colorChanger.addEventListener('click', changeBackgroundColor);

window.addEventListener("load", changeBackgroundColor);
